abstract class vehicle{
	abstract void speed();
	
}

class bike extends vehicle{
	void speed()
	{
		System.out.println("114/hr");
	}
}
class car extends vehicle{
	void speed()
	{
		System.out.println("223/hr");
	}
}
class truck extends vehicle{
	void speed()
	{
		System.out.println("445/hr");
	}
}

